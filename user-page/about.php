document.addEventListener('DOMContentLoaded', function () {
    const buttons = document.querySelectorAll('button');
    const tabs = document.querySelectorAll('.pos');

    // Hide all tabs initially
    tabs.forEach(tab => tab.style.display = 'none');

    buttons.forEach((btn, index) => {
        btn.addEventListener('click', function (e) {
            e.stopPropagation(); // prevent event bubbling

            const isVisible = tabs[index].style.display === 'block';

            // Hide all tabs first
            tabs.forEach(tab => tab.style.display = 'none');

            // Toggle current one
            tabs[index].style.display = isVisible ? 'none' : 'block';
        });
    });

    // Hide all if clicked outside
    document.addEventListener('click', function (e) {
        if (![...buttons, ...tabs].some(el => el.contains(e.target))) {
            tabs.forEach(tab => tab.style.display = 'none');
        }
    });
});
</script>

